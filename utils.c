/**
 * Writes \p size bytes of data from \p data to the file indicated by \p path.
 * @param path The path to the file to be written
 * @param data The data to be written
 * @param size The size in bytes of \p data
 */
int utils_dump(const char *path, const void *data, size_t size)
{
    FILE *fp = fopen(path, "wb");
    if (fp == NULL) {
        return -1;
    }

    size_t written = fwrite((uint8_t *)data, 1, size, fp);
    if (written < size) {
        fprintf(stderr, "Short write to %s.\n", path);
    }

    fclose(fp);
    return 0;
}

/**
 * Converts an array of palette indices into an array of RGBA values.
 * @param indices An array of palette indices to be converted
 * @param index_count The number of elements in \p indices
 */
uint8_t *utils_indexed_to_rgba(const uint8_t *indices, size_t index_count)
{
    if (indices == NULL) {
        return NULL;
    }

    uint8_t *rgba = calloc(index_count * 4, sizeof *rgba);
    if (rgba == NULL) {
        Engine.fatal("Couldn't allocate RGBA buffer.\n");
    }

    for (size_t i = 0; i < index_count; i++) {
        for (size_t j = 0; j < 3; j++) {
            rgba[4 * i + j] = palette[3 * indices[i] + j];
        }

        /*
         * 0xff represents full transparency in the Quake palette
         */
        if (indices[i] == 0xff) {
            rgba[4 * i + 3] = 0x00;
        } else {
            rgba[4 * i + 3] = 0xff;
        }
    }

    return rgba;
}

void *utils_read_binary_file(const char *path)
{
    FILE *fp = fopen(path, "rb");
    if (fp == NULL) {
        fprintf(stderr, "Couldn't open %s.\n", path);
        return NULL;
    }

    if (fseek(fp, 0, SEEK_END) != 0) {
        perror(path);
        return NULL;
    }

    const long file_size = ftell(fp);
    if (file_size == -1) {
        perror(path);
        return NULL;
    }

    rewind(fp);

    uint8_t *data = calloc(file_size, sizeof *data);
    if (data == NULL) {
        perror("model_from_bsp");
        return NULL;
    }

    const size_t read_size = fread(data, sizeof *data, file_size, fp);
    if ((long)read_size != file_size) {
        fputs("Short read.\n", stderr);
        return NULL;
    }

    fclose(fp);
    fp = NULL;

    return (void *)data;
}

